				<h3>The thought behind...</h3>
							
				<p>My personal interest was to make political information become more comprehensible. It is quite widespread in Thailand that politicians buy votes. People in many area have considerably poor education that they never understand how their vote effect their life. People usually receive bribe and give their vote away. Afterward, those evil politicians will become ministers and they will take their money back in an exceeding amount.</p>
				
				<p>Ten years ago, I was a big fan of the game "Simcity". I used to play a lot and I really like the simple logic of cause and effect behind the game which I think that is a mimetic resemblance of our every day life. So I think that it would be good if I can make the complicated issue become easy to understand, this might help people to make a better decision for their life.</p>
				
				<p>I was also greatly inspired by a video called <a target="window" href="http://vimeo.com/3261363">The Crisis of Credit</a> Visualised by Jonathan Jarvis. It tells the story about how did the crisis happened and he made it become so clear that I could understand the situation easily. Another master piece that inspiring me was a political campaign called <a target="window" href="http://www.sagmeister.com/node/189">Move our money</a> by Stefan Sagmeister. His idea was to prevail US government to reduce the budget for military and put it to the education and public health sector instead. He used very simple graphics to present the fact that the government was absurdly spending money on the military section.</p>
				
