				<h3>Skype for TV & Companion Device</h3>
				<h4>October 2013 – April 2014</h4>
				<p>Skype living room experience was the first product team I was in after joining Skype in September 2013. Our team was responsible for crafting delightful Skype experience on large screens and I was tasked to design a brand new Skype for TV web application.</p>
				
				<img class="large" src="../_images/skype_tv1.jpg">
				
				<p>The challenge in this project was particularly around understanding the user behaviour interacting with TV devices where the main interface is neither touchscreen nor mouse and keyboard, instead a very limited-function remote control. Navigating around on TV is not as easy as on mobile or PC, so we had to come up with a user interface that doesn't require much navigation.</p>
				
				
				<p>During the project I also explored how Skype TV could work with a companion device. The goal was to create an experience that allows user to seamlessly transfer calls between different devices, as well as to control the TV using touch screen. To communicate the idea to stakeholders, I created a prototype with HTML and Javascript using Node.js which enables PC and mobile to talk to each other. <a href="mailto:soramist@gmail.com"> Get in touch</a> if you would like to know more about it.</p>
				
