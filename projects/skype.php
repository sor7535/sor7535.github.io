				<h3 style="text-transform: none;">SKYPE FOR iOS</h3>
				<h4>June 2015 – Present</h4>
				<p>Skype on iOS has gone through a few different design iterations in the past. Before this update, Skype on iPhone had adopted Metro UI design pattern from Windows Phone which, we learnt, was rather difficult for iOS users to understand and get used to. For iPad, there was a lack of core features such as group calling and rich media messaging, whilst the general visual look and feel was in need of a refresh. The universal app re-design also gave us opportunity to ensure the Skype experience across all iOS devices felt more seamless and tightly connected.</p>
				
				<img class="large" src="../_images/skype_ios1.jpg">	
				
				<p>The redesign greatly focussed on improving usability and feature discoverability, which made the application become more natural and intuitive to navigate around. Whether on iPhone or iPad the experience is the same.</p>
				
				<p>Apart from the improved user experience, the application is now much more scalable. During the redesign, we factored in upcoming works in our roadmap and created a foundation that can accomodate new features. This helps streamline the development process because we can keep building thing on top of the structure we have.</p>
				
