				<h3>Design exploration</h3>
				<p>Owing to the globalisation and internet rage, people are changing their behaviour of communication. Many people prefer to spend more time immersing in social network communities such as Facebook and Myspace rather than having physical relationships with the one who lives next door. My approach was to design a device that will stimulate physical interaction among the target users which are students or people who live in small scale communal space such as apartment or corridor.</p>
				<p>I was also inspired by Tamagochi, japanese electronic pet. I tried to make the product to be simple and does not contain high technology components and more user friendly. The design directions were including...</p>
				<p>- Allow users to enjoy spending their time in the kitchen.</br>
				- Provide convenience to user during shopping time. </br>
				- Simplicity, user friendly, easy to approach.</br>
				- Provide technical and emotional solutions.
				</p>
