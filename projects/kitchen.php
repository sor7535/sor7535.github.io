				<h3>Final Design</h3>
				<p>Kitchen mate is a multi-purpose device for multi-user, which aims to create small scale physical social network in common living spaces such as student dormitory, in order to strengthen their relationships by stimulating social interaction among them focussing on the kitchen environment.</p>
				<p>The device composes of two parts, main body and a passive speaker. Users can use either together or separately. With magnetic surface it can stay one refrigerator in the kitchen. Users can synchronise database through wireless connection by using widget on their computer.</p>
