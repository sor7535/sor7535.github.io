				<p>Sweatify is a social network platform that allows people to find sport activities to attend. Players can filter what kind of sport they want to play, when and where they are convenient. It also makes organising sport group a seamless experience. Organisers can easily set up an event and share it through email invitation or social network. It allows organisers to keep track of attendees, communicate changes and updates, as well as automagically splitting and collecting the fee for them.</p>
				
				
