				<h2>Piggy Bank Reinvented</h2>
				<h4>Service Design, Storytelling & Experience Prototype</h4>
				
				<img class="large" src="../_images/lilla1.jpg">		
				
				<div class="separator-micro"></div>
									
				<p>This is the last project of my Master programme before we kick off our degree project next semester. The project is about tangible saving for kids in a digital age. In this project we collaborate with Swedbank who wants to promote sustainable financial habit to children in an early age. They want the concept to be a new symbol of saving and it has to be fit in future context where physical money is becoming obsolete.</p>
		
				
				<div class="separator"></div>
				
				<h3>The Idea</h3>
				
				<p>My initial idea is to incorporate Social gaming and storytelling into the design. Because I believe playing is a great way to engage children in learning. It helps them to assimilate information and concept more quickly and enduringly. Stories create emotional connection to objects which make us perceive value of things differently.</p>
				
				<img class="medium" src="../_images/lilla2.jpg">
				<div class="separator-nano"></div>
				<p>One of the biggest problems of turning money into digital is that people won’t be perceiving the value of it as before. The impulse to buy was always limited by the lack of dollar bills in wallet. Spending money was transparent but with the plastic card, people can’t obviously see the actual value anymore. Especially kids who grow up with it, they might think that money is not exist and they can just use the card to buy anything they want.</p>
				
				<p>In this project, I want to bring back the fun of collecting behaviour, and my ultimate goal is to make kids desperately want to save money!</p>
				
				<img class="medium" src="../_images/lilla3.jpg">	
				<img class="medium" src="../_images/lilla4.jpg">	
				
				<div class="separator"></div>
				
				<h3>The Concept</h3>
				
				<p>I came up with a concept called "Den Lilla Nötsamlaren" or "The little nut hoarder", which is a set of product+service that aims to build emotional connection with savings and spendings through Social gaming and Storytelling, in order to promote a good financial habit to kids.</p>
				<p>The squirrel house is used as a digital piggy bank. It is connected directly to the kid’s savings account and to internet, so whenever they put money in, it will be transferred directly to the bank account. Once it receive money, it will also issues some stickers for the kids so they can put into their saving book or the receipt.</p>
				
				<img class="large" src="../_images/lilla20.jpg">	
				
				<div class="separator"></div>
				
				<h3>Prototype</h3>
										
				<p>
				After nailing down the concept, I go on with the prototype. And according to the concept I will have a lot of work to do, it could be divided into 3 parts: First, the piggy bank itself which will be an artefact that kids will save their money in and it will give kids some stickers to collect. Second, graphic design, this comprises of short-term saving receipt, badges and a saving book. Third, screen based interface and saving log poster for kids.</p>

				<p>So I started with the most important one (and probably the most complicated). I’m making a piggy bank box that will collect money from the card through NFC. So I use RFID reader to do that and it will give sound and light feedback to the user. When the kid collect a hundred krona it will give a badge to them, and I use a servo motor to feed the paper. After putting things up together it seems to work pretty well. Only thing that doesn’t work properly is the sound. My USB sticks somehow are not compatible with this Vmusic player or actually the other way around, so annoying!</p>
				
				<img class="medium" src="../_images/lilla8.jpg">	
				<img class="medium" src="../_images/lilla9.jpg">	
				<img class="medium" src="../_images/lilla5.jpg">	
				
				<div class="separator"></div>
				
				<h3>Final Design</h3>
										
				<p>Once kids open a savings account with the bank, they will receive a little squirrel house, and a story book. The squirrel house serves as a digital piggy bank and is connected directly to the their savings account. Children can use their money card to deposit money in (just to mention, this project is based on the thought that kids will receive their allowance in the form of NFC prepaid card). When they have saved up to certain amount, the device will issue a sticker badge that represents the savings for them to collect.</p>

				<p>The sticker badges come in variations and it depends on different occasion. For example if a girl receive money gift from her granny, it will print out granny badges, or if she saves some money on Christmas day, she will get a Christmas badge instead.</p>

				<p>Kids can either collect the stickers in story book which represents long-term savings or in the receipts for short-term goals. In the book, there will be a series of stories of 'The Little Nut Hoarder' that aim to teach kids about the reasons why saving is important as well as how can they improve their financial habit. For the short-term savings, parents can help kids setting up the goals and printing out receipts for them to save up. This will make savings and spendings more tangible and fun, and ultimately give kids awareness of the value of things.	</p>	
				
