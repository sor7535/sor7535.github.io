				<h3>Concept</h3>
				<p>The concept has four parts including Core value, Service Blueprint, Communication, and Growth Strategy. Core value is about our mission, who we are, and what we believe in. Service blueprint is the framework of the products and services we will provide to our users. Communication is more about the way we represent ourselves, the tone of voice we use to communicate to our users, as well as the visual design language. Last but not least, growth strategy is the action plan of how we will implement the concept and design.</p>
				<img class="medium"  src="../_images/sync6.jpg">
				
				<div class="separator"></div>
				<h3>core value</h3>
				<p>The sync is a social enterprise with a mission to build a creative community that fosters social innovation in Thailand. There are three essential tasks that we want to do for the society including creating culture, building community, and Repositioning social innovation.</p>
				<img class="medium"  src="../_images/sync7.jpg">
				
				<div class="separator"></div>
				<h3>service blueprint</h3>
				<p>While I was developing the concept, I struggled to build a service blueprint as I have found that creating social innovation is neither a linear nor logical process. Firstly, I tried to put all the service features into a conventional user journey framework (linear) but it didn’t seem to fit because the possibility of journeys is more than one. There are also several types of users which could enter the service through different channels and they will go through their own unique journey with a unique experience.</p>

				<p>I tried look at this project at a systemic level, then I found that what I was trying to create is actually an ecosystem that contains several services with multiple touch points, and all of them are interconnected in some ways. Users do not necessarily need to go through every service features but instead they can just pick whatever they need in the system. I tried to created a framework that could explain this system and I’ve came up with <a href="../_images/sync_metro.jpg" target="blank">this one</a>.</p>
				<a href="../_images/sync_metro.jpg" target="blank"><img class="medium" src="../_images/sync8.jpg"></a>
				<div class="separator-nano"></div>
				<p>This framework consists of two axises. The horizontal represents service strategy (encourage, equip and empower) and the vertical is for the main touchpoints (online platform, events & activities, and physical space). All the service features can be placed somewhere in this diagram.</p>

				<p>This framework has helped me a lot when it comes to describing user journeys as it provides an overview of the whole service. It also allows me to freely create connections between different service features as well as to see the relationship between different users.</p>
				
				
