				<h2>The Sync</h2>
				<h4>Master Thesis - Design & Social Innovation</h4>
				<img class="large" src="../_images/sync0.jpg">		
				
				<div class="separator-micro"></div>
									
				<p>Many people dream of making a change to our society. But most of the time, they are struggling to turn their dream into reality. Passion alone won't bring change. There are many difficulties that people have to face when it comes to creating innovation, particularly, social innovation.</p>
				<p>This project I collaborate with ChangeFusion, a non-profit organisation based in Bangkok, and our goal is to create a community for social innovation in Thailand. My personal focus is on integrating design (as a process) into the core culture of this community. I want people to understand better the importance of design, not just as a tool to make thing more beautiful, but a way to approach problems. And I believe it will give them a better chance to succeed in whatever they do.</p>
				
				
	
				
				
				<div class="separator"></div>
				
				
				<h3>Approach</h3>
				<p>Initially, I planned to work in a typical workflow of a design project. Starting from research, ideate, conceptualise, design and evaluate and then start implementing. But once I discussed with my collaborator I found that this is actually an on-going project and they planned to launch this in the middle of May (even before the the final examination). So most of the things such as core concept, identity, website, space, and some materials have to be done by then. So I had to change my approach a quite a lot from the beginning.</p>

				<p>My workflow has to be quite flexible, so I split the project into multiple iterations regarding to the their timeline. Each has a cycle of research, ideation, design and implement and focuses on the deliverable. This is quite different from the typical (UID) design process, but more like lean startup, which focusses on validating ideas, and gaining user feedback through minimum viable products</p>
				
				
				<img class="medium" src="../_images/sync2.png">
				
				<div class="separator"></div>
				
				<h3>Insights</h3>
				<p>In this project I divided my research into two parts: 'Looking Out' and 'Looking In'. “Looking Out” focuses on exploring what’s out there, scrutinising the world of design and social innovation to understand more about them and see how they can be overlapped. While “Looking In” focuses more on understanding the context of Thailand, studying how is creative and entrepreneurial scene in the country. Are there any movements happening, and what is the trend? As well as looking at the culture and the perception of Thai people about design and social innovation.</p>
				
				<div class="separator"></div>
				<h3>design culture</h3>
				<p>One of the most crucial components in design is design process. It is a sequence of activities that we perform to achieve our design tasks. Different people has different process and it’s also depended on the task itself and it’s normally not so linear as you have to reiterate many times before getting to the final result.</p>
				<p>There is one common pattern that explain most of the design processes. It has three steps: open, explore and close. ‘Open’ is about setting a goal. You start by asking a question, to scope what you are trying to achieve, then you ‘explore’ all the possibilities in the focussed area. On the way you can experiment new ideas and examine to validate them. ‘Close’ is to make sense of what you have found and to consolidate them to the final conclusion.</p>
				<img class="medium" src="../_images/sync3.jpg">
				
				
				<div class="separator"></div>
				<h3>context</h3>
				<p>During the project, I went back to Thailand to do my research. My aim of this was to understand the perception of Thai people about design and social innovation. I would like to know how does design mean to them, how do they approach it and if they use it to create social innovation.</p>
				<p>While I was there I interviewed a number of people from various backgrounds such as designer, entrepreneur, developer and educator. This is because the sync will be a place that support everyone in the society, doesn’t matter who they are and what they do, so I think it will be valuable to hear a broad range of perspective. I’ve found that many people in Thailand are not aware about social innovation yet. When I ask them about social innovation, they would think of something related to non-profit organisation, volunteering, or even charity work. </p>
				<p>I've also found that most of people don’t really see the value of design, particularly in the business setting. They tend to think that design is all about making things more beautiful. People see design as the sugar coating rather than the core of product development. In Thailand, design is not appreciated as the way it’s supposed to be.</p>				
				<img class="medium" src="../_images/sync4.jpg">
				
				
