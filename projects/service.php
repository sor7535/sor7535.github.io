				<h2>Umeå Social Services</h2>
				<h4>Service Design</h4>
				
				<img class="large" src="../_images/service0.jpg">
				
				<div class="separator-micro"></div>
				
								
				
				<p>This project was done in collaboration with Sogeti, an IT consultancy, who was working with Social services department of Umeå Municipality to develop an IT system to support the internal work processes within the organisation in order to delivery quality public services to the community.</p>
				<p>We, as a team comprised of Camila Lima and myself, aimed to design a service solution that will help the Social Services Department sharing information easily and more meaningfully, as well as to improve the service itself.</p>
				
				<img class="medium" src="../_images/service1.jpg">
				
				<div class="separator"></div>
				
				<h3>Ethnographic Research</h3>		
				<p>At the beginning, we did not have any direction to follow, instead we were encouraged to freely explore by doing ethnographic research and find our own design focus. We went with open mind to Interview with employees from many levels in the Social Services organisation to get better understanding about how the organisation is structured and figured out the communication flow both inside and outside the organisation. Not only getting information from the employee we were also curious about the customer point of view and We found a customer who has been using the service for years to interview with.</p>
				<p>The observations and interviews help us to identify the key situations. We create a short animation to explain how the organisation works and also the describe the key situation we found.</p>
				
				<div class="videoWrapper">
					<iframe src="http://player.vimeo.com/video/11634906?badge=0&amp;color=97d4dc" width="620" height="349" frameborder="0" webkitAllowFullScreen mozallowfullscreen allowFullScreen></iframe>
				</div>
				
				<div class="separator"></div>
				
				<h3>User Journey</h3>
				<p>After gathering enough information, we analysed and mapped out all the possibility of user journey as well as the communication flow throughout the whole service ecosystem.</p> <p>We also held a workshop session which we set up a scenario and ask people to play on the given roles which were the people who involve in the service. We observed and discussed with the players about how do they think and feel while they were reenacting the roles in order to define what should be improved.</p>
				
				<a href="../_images/service_journey.png" target="blank"><img class="large" src="../_images/service2.png"></a>
				<div class="separator-nano"></div>
				<img class="medium" src="../_images/service4.jpg">
				
				<div class="separator"></div>
				
				<h3>Refurbishing User Experience</h3>
				<p>There were two main issues that we found problematic. First, the lack of transparency in the system, because in the current system, the customer have problem to access to their own information, they need to contact the social secretary every time they want to know about their detail and status. In the organisation, employees have very limit accessibility to the customer information, they can not access to the customer archive due to the privacy policy, which many time, information in their archive are valuable, because employees can use previous cases as references so that they can make a decision quicker and better. Also the third party organisations, which the customers can get the service through them as well. Although their jobs are strongly relate to the social services, they don't have the accessibility either.</p>
				<p>The second point that we wanted to improve was about the organisation identity. From the result of our research, we found that people perceive the social services as a disgraceful place to go. They feel that asking for help from the social service is shameful and commit as the last place to go. This issue is very sensitive because it's about culture and society and we think that it is very important to change the way the society perceive social services, because one of the main problem is that people are ashamed asking for help even though they really need it and many times their problems get worse and finally become too late to solve.</p>
				<p>Before continuing to the ideation process, we set up our goal to achieve which is to refurbish the whole service ecosystems.</p>
				<img class="medium" src="../_images/service5_2.png">
				<div class="separator-nano"></div>
				<img class="medium" src="../_images/service15.jpg">
				
				
				<div class="separator"></div>
				
				<h3>Ideas Generation</h3>
				<p>Many heads are better than two heads, so we lured people around the school with coffee & cookie to join our brainstorm session which focused on the repositioning strategies for the organisation such as how to make the social services become more friendly so that people may feel more comfortable to come to ask for help or how can we convince people to share their problems and how to get rid of the "Shame" that people feel. We also brainstormed on the visual graphic and colour which was very useful when we were designing the Corporate Identity of the organisation and it's look & feel.</p>
				<img class="medium" src="../_images/service6.jpg">
				<img class="medium" src="../_images/service7.jpg">
				
				<div class="separator"></div>
				
				<h3>Repositioning Strategy</h3>
				<p>We created a new Corporate identity for Umeå social services, from the result of brainstorm session we came up with 3 keywords, Friendly, Security and Reliable which we use as the core messages. We picked birch (which is the iconic tree of Umeå city) to represent the organisation which communicates as the "Home" of the birds (citizens). We also suggested that the graphic identity has to be consistent among all the touchpoints including website, brochure, reception as well as non profit organisation that are affiliated with Umeå social services.</p>
				<img class="medium" src="../_images/service8.jpg">
				
				
				<div class="separator"></div>
				
				<h3>Improving Transparency</h3>
				<p>Another issue we tackled was the transparency of system. After we broke down all the information layers and communication flow, we reconsidered to make relevant information become visible to relevant users as well as to make it easier to attain.</p>
				<a href="../_images/service9_2.png" target="blank"><img class="medium" src="../_images/service9.jpg"></a>
				
				<a href="../_images/service_wireframe.pdf" target="blank"><img class="medium" src="../_images/service16.jpg"></a>
				
				<div class="separator"></div>
				
				<h3>Final Design</h3>
				<p>Finally, we came up with the idea of using social network application to connect all the user together and use information filtering system to keep customers' privacy. By splitting customer's information into two layers, first for personal data which can only be accessed by relevant users and second for the case detail that is available for the social worker to use as references. Customers can also access to their own information so they can alway track their status.</p>
				<p>This animation shows a possible scenario when this new service ecosystem is implemented to Umeå Social Services.</p>
				
